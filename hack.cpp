#include<bits/stdc++.h>
#define ll long long
using namespace std;
int main() {
    cout << 1 << endl;
    ll n = 100000;
    cout << n << endl;
    for(ll i = 1 ; i <= n ; i++) {
        cout << n << " ";
        for(ll j = 1 ; j <= n ; j++) {
            cout << j << " ";
        }
        cout << endl;
    }
}
